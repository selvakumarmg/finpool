// Fallback: ensure non-implemented languages reuse english strings
const englishTree = translations.en;

(['ta', 'ml', 'hi', 'te', 'ka'] as SupportedLanguage[]).forEach((lang) => {
  translations[lang] = {
    ...englishTree,
    ...translations[lang],
  };
});

export type TranslationKey = string;

export const getTranslation = (language: SupportedLanguage, key: TranslationKey, params?: Record<string, string | number>) => {
  const segments = key.split('.');
  let current: TranslationTree | string | undefined = translations[language];

  for (const segment of segments) {
    if (typeof current === 'string') break;
    current = current?.[segment];
  }

  if (typeof current !== 'string') {
    // fallback to english
    current = segments.reduce((acc: TranslationTree | string | undefined, segment) => {
      if (typeof acc === 'string') return acc;
      return acc?.[segment];
    }, englishTree) as string | undefined;
  }

  if (typeof current !== 'string') {
    return key;
  }

  if (params) {
    return Object.entries(params).reduce(
      (acc, [paramKey, value]) => acc.replace(`{{${paramKey}}}`, String(value)),
      current
    );
  }

  return current;
};

